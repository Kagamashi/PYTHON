'''
int: Whole numbers (positive or negative), e.g., 5, -10.
float: Decimal (floating-point) numbers, e.g., 3.14, -0.001.
string (str): Sequence of characters, e.g., "Hello", 'Python'.
boolean (bool): Represents two values: True or False.

We can check data type of variable using type() function
'''

# Integers and Floats
a = 5  # int
b = 2.5  # float

# Strings
greeting = "Hello, World!"  # string

# Booleans
is_active = True  # bool

# Checking types
print(type(a))  # Output: <class 'int'>
print(type(b))  # Output: <class 'float'>
print(type(greeting))  # Output: <class 'str'>
print(type(is_active))  # Output: <class 'bool'>
