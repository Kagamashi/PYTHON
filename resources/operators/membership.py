'''
in: Returns True if a value exists in the sequence.
not in: Returns True if a value does not exist in the sequence.
'''

my_list = [1, 2, 3, 4, 5]
my_string = "hello"

print(3 in my_list)  # Output: True (3 is in the list)
print(6 not in my_list)  # Output: True (6 is not in the list)
print('h' in my_string)  # Output: True ('h' is in the string)
