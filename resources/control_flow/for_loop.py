'''
for loop is used to iterate over a sequence (list, tuple, string) or any other literable object
it allows to execute a block of code for each item in the sequence
'''

# Iterate over a list
numbers = [1, 2, 3, 4]
for number in numbers:
    print(number)

# Iterate over a string
for char in "Python":
    print(char)

# Output:
# 1
# 2
# 3
# 4
# P
# y
# t
# h
# o
# n
